//Comparison Operators
console.log("Comparison Operators");
const x = 3, y = 4;
console.log("x==y: ",x==y);// Loose equality
console.log("x===y: ",x===y);// Strict equality
console.log("x!=y: ",x!=y);// Loose equality
console.log("x!==y: ",x!==y);// Strict equality
console.log("x>3: ",x>3);
console.log("x<3: ",x<3);
console.log("x>=3: ",x>=3);
console.log("x<=3: ",x<=3);