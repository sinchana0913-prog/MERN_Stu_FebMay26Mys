// While loop
console.log("While loop");

let count = 0; // forward while loop
while(count<5){
    console.log("count: ",count);
    count++;
}

let i = 5; // backward while loop
while(i>0){
    console.log("i: ",i);
    i--;
}
// do while
let value = 0;
do{
    console.log("Value:",value);
    value++;
}while(value < 3);

